function clearinfo()
{
	var workname=document.getElementById("workname")
	var worksowership=document.getElementsByName("queryBean.worksowership");
    var media=document.getElementsByName("queryBean.media");
	var status=document.getElementsByName("queryBean.status");
	var begin=document.getElementById("begin");
	var end=document.getElementById("end");
	workname.checked=false
	worksowership.checked=false
	media.checked=false
	status.value=""
	media.value=""
	status.value=""
	begin.value=""
	end.value=""
	removeAll();
}